// Increment operators

#include <stdio.h>

int main()
{
    int a = 5;
    printf("The value after a++ is %d\n", a++); // pehla print pher increment
    printf("The value of a is %d\n", a);

    int b = 5;
    printf("The value after ++b is %d\n", ++b); // pehla increment pher print
    printf("The value of b is %d\n", b);

    // Similar scenario will be there for rest of the operators

    return 0;
}